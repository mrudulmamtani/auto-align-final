"""
Renderer — converts a DraftOutput into a Word DOCX with a Traceability Annex.
Produces two files in OUTPUT_DIR:
  <doc_id>.docx           — the governance document
  <doc_id>_traceability.docx — the traceability annex table
"""
import io
import os
from datetime import datetime, timezone
from docx import Document
from docx.shared import Pt, RGBColor, Inches
from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.oxml.ns import qn
from docx.oxml import OxmlElement

from .models import (
    DraftOutput, ControlBundle, PolicyDraftOutput, GOLDEN_TOC,
    StandardDraftOutput, STANDARD_TOC,
    ProcedureDraftOutput, PROCEDURE_TOC,
)
from .config import OUTPUT_DIR


def _set_cell_bg(cell, hex_color: str):
    """Set table cell background color."""
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:fill"), hex_color)
    shd.set(qn("w:val"), "clear")
    tcPr.append(shd)


def _add_heading(doc: Document, text: str, level: int):
    p = doc.add_heading(text, level=level)
    return p


def _build_main_doc(draft: DraftOutput) -> Document:
    doc = Document()

    # Title page
    title = doc.add_paragraph()
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = title.add_run(f"{draft.doc_type.upper()}: {draft.topic}")
    run.bold = True
    run.font.size = Pt(22)

    meta = doc.add_paragraph()
    meta.alignment = WD_ALIGN_PARAGRAPH.CENTER
    meta.add_run(
        f"Organization: {draft.org_name}\n"
        f"Version: {draft.version}\n"
        f"Document ID: {draft.doc_id}\n"
        f"Generated: {draft.generated_at[:10]}"
    ).font.size = Pt(11)

    doc.add_page_break()

    # Table of contents placeholder
    _add_heading(doc, "Table of Contents", 1)
    doc.add_paragraph("[Auto-generated table of contents — update field in Word to populate]")
    doc.add_page_break()

    # Sections
    for s in draft.sections:
        _add_heading(doc, f"{s.section_id}  {s.title}", 1)
        purpose_para = doc.add_paragraph()
        purpose_para.add_run("Purpose: ").bold = True
        purpose_para.add_run(s.purpose)

        for r in s.requirements:
            req_para = doc.add_paragraph(style="List Bullet")
            id_run = req_para.add_run(f"[{r.req_id}]  ")
            id_run.bold = True
            id_run.font.color.rgb = RGBColor(0x1F, 0x49, 0x7D)
            req_para.add_run(r.text)

            # Citation footnote
            if r.citations:
                cite_para = doc.add_paragraph(style="List Bullet 2")
                cite_para.add_run("Controls cited: ").italic = True
                cite_para.add_run(", ".join(r.mapped_control_ids or r.citations)).font.size = Pt(9)

    doc.add_page_break()

    # Footer note
    footer_para = doc.add_paragraph()
    footer_para.alignment = WD_ALIGN_PARAGRAPH.CENTER
    footer_para.add_run(
        f"Generated by AutoAlign Policy Factory | {datetime.now(timezone.utc).strftime('%Y-%m-%d')}"
    ).font.size = Pt(8)

    return doc


def _build_traceability_annex(
    draft: DraftOutput,
    bundles: list[ControlBundle],
) -> Document:
    doc = Document()

    _add_heading(doc, "Traceability Annex", 1)
    sub = doc.add_paragraph()
    sub.add_run(
        f"Document: {draft.doc_id}  |  {draft.doc_type}: {draft.topic}  |  "
        f"Org: {draft.org_name}  |  Version: {draft.version}"
    ).font.size = Pt(10)

    doc.add_paragraph(
        "This annex maps every requirement to the cybersecurity controls that ground it. "
        "Every normative requirement (SHALL/MUST/REQUIRED/WILL) must have at least one cited control."
    )

    # Build lookup: chunk_id -> bundle
    bundle_map = {b.chunk_id: b for b in bundles}

    # Table: Req ID | Normative | Requirement Text | Cited Chunk IDs | Control IDs | UAE IA ID
    headers = ["Req ID", "Norm?", "Requirement Text", "Cited Chunk IDs", "NIST Control IDs", "UAE IA ID"]
    table = doc.add_table(rows=1, cols=len(headers))
    table.style = "Table Grid"

    hdr_row = table.rows[0]
    for i, h in enumerate(headers):
        cell = hdr_row.cells[i]
        cell.text = h
        _set_cell_bg(cell, "1F497D")
        for para in cell.paragraphs:
            for run in para.runs:
                run.bold = True
                run.font.color.rgb = RGBColor(0xFF, 0xFF, 0xFF)
                run.font.size = Pt(9)

    for s in draft.sections:
        # Section separator row
        sep_row = table.add_row()
        sep_cell = sep_row.cells[0]
        sep_cell.merge(sep_row.cells[-1])
        sep_cell.text = f"Section {s.section_id}: {s.title}"
        _set_cell_bg(sep_cell, "D9E1F2")
        for para in sep_cell.paragraphs:
            for run in para.runs:
                run.bold = True
                run.font.size = Pt(9)

        for r in s.requirements:
            row = table.add_row()
            cells = row.cells

            cells[0].text = r.req_id
            cells[1].text = "YES" if r.is_normative else "no"

            # Truncate long requirement text for readability
            cells[2].text = r.text[:300] + ("..." if len(r.text) > 300 else "")

            cells[3].text = "\n".join(r.citations) if r.citations else "-"

            # Resolve control IDs from bundles where possible
            resolved_ids: list[str] = []
            uae_ids: list[str] = []
            for cid in r.citations:
                b = bundle_map.get(cid)
                if b:
                    resolved_ids.append(b.control_id)
                    if b.uae_ia_id:
                        uae_ids.append(b.uae_ia_id)
            cells[4].text = "\n".join(resolved_ids) if resolved_ids else ", ".join(r.mapped_control_ids) or "-"
            cells[5].text = "\n".join(sorted(set(uae_ids))) if uae_ids else "-"

            # Colour-code normative rows
            if r.is_normative:
                _set_cell_bg(cells[1], "E2EFDA")

            for cell in cells:
                for para in cell.paragraphs:
                    for run in para.runs:
                        run.font.size = Pt(8)

    return doc


def render(
    draft: DraftOutput,
    bundles: list[ControlBundle],
    output_dir: str = OUTPUT_DIR,
) -> tuple[str, str]:
    """
    Write main DOCX and traceability annex to output_dir.
    Returns (main_path, annex_path).
    """
    os.makedirs(output_dir, exist_ok=True)

    safe_id = draft.doc_id.replace("/", "-").replace("\\", "-")
    main_path   = os.path.join(output_dir, f"{safe_id}.docx")
    annex_path  = os.path.join(output_dir, f"{safe_id}_traceability.docx")

    print(f"[Renderer] Writing main document -> {main_path}")
    main_doc = _build_main_doc(draft)
    main_doc.save(main_path)

    print(f"[Renderer] Writing traceability annex -> {annex_path}")
    annex_doc = _build_traceability_annex(draft, bundles)
    annex_doc.save(annex_path)

    print(f"[Renderer] Done. {sum(1 for r in draft.all_requirements)} requirements rendered, "
          f"{sum(1 for r in draft.all_requirements if r.is_normative)} normative.")
    return main_path, annex_path


