_NAVY  = "1F497D"
_STEEL = "4472C4"
_LIGHT = "D9E1F2"
_GREEN = "E2EFDA"
_WHITE = "FFFFFF"

_ROLE_LABELS = {
    "authority_owner_delegates": "Authority / Owner / Delegates",
    "legal":                     "Legal",
    "internal_audit":            "Internal Audit",
    "HR":                        "Human Resources",
    "cybersecurity":             "Cybersecurity Function",
    "other_departments":         "Other Departments",
    "all_staff":                 "All Staff",
}


def _hdr(doc: Document, text: str, level: int = 1):
    p = doc.add_heading(text, level=level)
    for run in p.runs:
        run.font.color.rgb = RGBColor(0x1F, 0x49, 0x7D)
    return p


def _tbl_hdr_row(table, headers: list[str], bg: str = _NAVY):
    row = table.rows[0]
    for i, h in enumerate(headers):
        c = row.cells[i]
        c.text = h
        _set_cell_bg(c, bg)
        for para in c.paragraphs:
            for run in para.runs:
                run.bold = True
                run.font.color.rgb = RGBColor(0xFF, 0xFF, 0xFF)
                run.font.size = Pt(9)


def _small(cell):
    for para in cell.paragraphs:
        for run in para.runs:
            run.font.size = Pt(8)


def render_policy(
    draft: PolicyDraftOutput,
    bundles: list[ControlBundle],
    output_dir: str = OUTPUT_DIR,
) -> tuple[str, str]:
    """
    Render a Golden Policy Baseline document as two DOCX files:
      <doc_id>.docx               — formatted policy document
      <doc_id>_traceability.docx  — control traceability annex with cross-encoder scores
    Returns (main_path, annex_path).
    """
    os.makedirs(output_dir, exist_ok=True)
    safe_id    = draft.doc_id.replace("/", "-")
    main_path  = os.path.join(output_dir, f"{safe_id}.docx")
    annex_path = os.path.join(output_dir, f"{safe_id}_traceability.docx")

    bundle_map = {b.chunk_id: b for b in bundles}
    ctrl_map   = {b.control_id: b for b in bundles}

    # ── Main Policy Document ──────────────────────────────────────────────────
    doc = Document()

    # Cover block
    cover = doc.add_paragraph()
    cover.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r = cover.add_run(draft.title_en.upper())
    r.bold = True; r.font.size = Pt(20)
    r.font.color.rgb = RGBColor(0x1F, 0x49, 0x7D)

    meta_lines = [
        f"Document ID: {draft.doc_id}",
        f"Version: {draft.version}",
        f"Effective Date: {draft.effective_date}",
        f"Owner: {draft.owner}",
        f"Classification: {draft.classification}",
        f"Generated: {draft.generated_at[:10]}",
    ]
    for line in meta_lines:
        p = doc.add_paragraph()
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        p.add_run(line).font.size = Pt(11)

    doc.add_page_break()

    # Table of Contents (static — exact 6 headings)
    _hdr(doc, "Table of Contents")
    for i, heading in enumerate(GOLDEN_TOC, start=1):
        doc.add_paragraph(f"{i}.  {heading}", style="List Number")
    doc.add_page_break()

    # 1. Objectives
    _hdr(doc, "1.  Objectives")
    doc.add_paragraph(draft.objectives_en)

    # 2. Scope and Applicability
    _hdr(doc, "2.  Scope and Applicability")
    doc.add_paragraph(draft.scope_applicability_en)

    # 3. Policy Elements
    _hdr(doc, "3.  Policy Elements")
    for e in draft.policy_elements:
        p = doc.add_paragraph(style="List Bullet")
        r_id = p.add_run(f"Element {e.element_no}.  ")
        r_id.bold = True
        r_id.font.color.rgb = RGBColor(0x1F, 0x49, 0x7D)
        p.add_run(e.statement_en)
        if e.trace:
            refs = ", ".join(t.control_id for t in e.trace)
            note = doc.add_paragraph(style="List Bullet 2")
            note.add_run(f"Controls: {refs}").font.size = Pt(9)

    # 3a. Policy Sub-programs (element 2 expansion)
    _hdr(doc, "3a.  Cybersecurity Policy Suite (Elements 2-1 to 2-34)", level=2)
    tbl = doc.add_table(rows=1, cols=3)
    tbl.style = "Table Grid"
    _tbl_hdr_row(tbl, ["Sub-No", "Name", "Objective"])
    for sp in draft.policy_subprograms:
        row = tbl.add_row()
        row.cells[0].text = sp.sub_no
        row.cells[1].text = sp.name_en
        row.cells[2].text = sp.objective_en
        if sp.references:
            row.cells[2].text += f"\n[Ref: {', '.join(sp.references)}]"
        for cell in row.cells:
            _small(cell)

    # 4. Roles and Responsibilities
    _hdr(doc, "4.  Roles and Responsibilities")
    rr = draft.roles_responsibilities
    for role_key, label in _ROLE_LABELS.items():
        items = getattr(rr, role_key, [])
        _hdr(doc, label, level=2)
        for item in items:
            doc.add_paragraph(item, style="List Bullet")

    # 5. Policy Compliance
    _hdr(doc, "5.  Policy Compliance")
    for i, clause in enumerate(draft.compliance_clauses, start=1):
        p = doc.add_paragraph(style="List Number")
        p.add_run(clause)

    # 6. Exceptions
    _hdr(doc, "6.  Exceptions")
    doc.add_paragraph(draft.exceptions_en)

    # Closing note
    _hdr(doc, "Note", level=2)
    note_p = doc.add_paragraph()
    note_p.add_run(draft.closing_note_en).italic = True

    doc.add_page_break()
    footer_p = doc.add_paragraph()
    footer_p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    footer_p.add_run(
        f"Generated by AutoAlign Policy Factory (Golden Policy Baseline) | "
        f"{datetime.now(timezone.utc).strftime('%Y-%m-%d')}"
    ).font.size = Pt(8)

    print(f"[Renderer] Writing policy -> {main_path}")
    doc.save(main_path)

    # ── Traceability Annex (cross-encoder sourced controls) ───────────────────
    ann = Document()
    _hdr(ann, "Control Traceability Annex")
    ann.add_paragraph(
        f"Document: {draft.doc_id}  |  {draft.title_en}  |  Version: {draft.version}"
    ).runs[0].font.size = Pt(10)
    ann.add_paragraph(
        "All controls below were sourced via dense retrieval + cross-encoder reranking "
        "(top-20 per query). The 'Rerank Score' column shows the cross-encoder relevance "
        "score for each control relative to its retrieval query. Higher scores indicate "
        "stronger semantic relevance."
    ).runs[0].font.size = Pt(9)

    # Table: Element | Statement (truncated) | Framework | Control ID | Title | NCA ID | Rerank Score
    hdrs = ["Element", "Statement (excerpt)", "Framework", "Control ID", "Control Title", "NCA ID", "Rerank Score"]
    tbl2 = ann.add_table(rows=1, cols=len(hdrs))
    tbl2.style = "Table Grid"
    _tbl_hdr_row(tbl2, hdrs)

    for e in draft.policy_elements:
        for t in e.trace:
            row = tbl2.add_row()
            cells = row.cells
            cells[0].text = f"Element {e.element_no}"
            cells[1].text = e.statement_en[:200] + ("..." if len(e.statement_en) > 200 else "")

            # Resolve from bundle maps
            b_by_id    = ctrl_map.get(t.control_id)
            b_by_chunk = bundle_map.get(t.source_ref)
            b = b_by_id or b_by_chunk

            cells[2].text = t.framework
            cells[3].text = t.control_id
            cells[4].text = b.title if b else "-"
            cells[5].text = (b.nca_id or b.uae_ia_id or "-") if b else "-"
            score = b.rerank_score if b else None
            cells[6].text = f"{score:.4f}" if score is not None else "-"

            if score is not None and score > 5.0:
                _set_cell_bg(cells[6], _GREEN)
            for cell in cells:
                _small(cell)

    # Subprogram catalog table in annex
    _hdr(ann, "Cybersecurity Policy Suite — Full Catalog", level=2)
    tbl3 = ann.add_table(rows=1, cols=4)
    tbl3.style = "Table Grid"
    _tbl_hdr_row(tbl3, ["Sub-No", "Name", "Objective", "References"])
    for sp in draft.policy_subprograms:
        row = tbl3.add_row()
        row.cells[0].text = sp.sub_no
        row.cells[1].text = sp.name_en
        row.cells[2].text = sp.objective_en
        row.cells[3].text = ", ".join(sp.references) if sp.references else "-"
        for cell in row.cells:
            _small(cell)

    print(f"[Renderer] Writing traceability annex -> {annex_path}")
    ann.save(annex_path)

    print(f"[Renderer] Policy render done: {len(draft.policy_elements)} elements, "
          f"{len(draft.policy_subprograms)} subprograms.")
    return main_path, annex_path


