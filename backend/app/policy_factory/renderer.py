def render_standard(
    draft: StandardDraftOutput,
    bundles: list[ControlBundle],
    output_dir: str = OUTPUT_DIR,
) -> tuple[str, str]:
    """
    Render a Golden Standard Baseline document as two DOCX files:
      <doc_id>.docx               — formatted standard document
      <doc_id>_traceability.docx  — control traceability annex with cross-encoder scores
    Returns (main_path, annex_path).
    """
    os.makedirs(output_dir, exist_ok=True)
    safe_id    = draft.doc_id.replace("/", "-")
    main_path  = os.path.join(output_dir, f"{safe_id}.docx")
    annex_path = os.path.join(output_dir, f"{safe_id}_traceability.docx")

    ctrl_map  = {b.control_id: b for b in bundles}
    chunk_map = {b.chunk_id:   b for b in bundles}

    # ── Main Standard Document ────────────────────────────────────────────────
    doc = Document()

    # Cover block
    cover = doc.add_paragraph()
    cover.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r = cover.add_run(draft.title_en.upper())
    r.bold = True; r.font.size = Pt(20)
    r.font.color.rgb = RGBColor(0x1F, 0x49, 0x7D)

    for line in [
        f"Document ID: {draft.doc_id}",
        f"Version: {draft.version}",
        f"Effective Date: {draft.effective_date}",
        f"Owner: {draft.owner}",
        f"Classification: {draft.classification}",
        f"Generated: {draft.generated_at[:10]}",
    ]:
        p = doc.add_paragraph()
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        p.add_run(line).font.size = Pt(11)

    doc.add_page_break()

    # Table of Contents (static — exact 6 headings)
    _hdr(doc, "Table of Contents")
    for i, heading in enumerate(STANDARD_TOC, start=1):
        doc.add_paragraph(f"{i}.  {heading}", style="List Number")
    doc.add_page_break()

    # Definitions and Terms
    _hdr(doc, "Definitions and Terms")
    tbl_def = doc.add_table(rows=1, cols=2)
    tbl_def.style = "Table Grid"
    _tbl_hdr_row(tbl_def, ["Term", "Definition"])
    for defn in draft.definitions:
        row = tbl_def.add_row()
        row.cells[0].text = defn.term_en
        row.cells[1].text = defn.description_en
        for cell in row.cells:
            _small(cell)

    doc.add_page_break()

    # 1. Objectives
    _hdr(doc, "1.  Objectives")
    doc.add_paragraph(draft.objectives_en)

    # 2. Scope and Applicability
    _hdr(doc, "2.  Scope and Applicability")
    doc.add_paragraph(draft.scope_en)

    # 3. Standards Requirements
    _hdr(doc, "3.  Standards Requirements")
    for dom in draft.domains:
        _hdr(doc, f"Domain {dom.domain_number}: {dom.title_en}", level=2)

        p_obj = doc.add_paragraph()
        p_obj.add_run("Objective: ").bold = True
        p_obj.add_run(dom.objective_en)

        p_risk = doc.add_paragraph()
        p_risk.add_run("Potential Risks: ").bold = True
        p_risk.add_run(dom.potential_risks_en)

        _hdr(doc, "Requirements:", level=3)
        for req in dom.requirements:
            p_req = doc.add_paragraph(style="List Bullet")
            r_id = p_req.add_run(f"{req.req_id}  ")
            r_id.bold = True
            r_id.font.color.rgb = RGBColor(0x1F, 0x49, 0x7D)
            p_req.add_run(req.statement_en)
            if req.trace:
                refs = ", ".join(t.control_id for t in req.trace)
                note = doc.add_paragraph(style="List Bullet 2")
                note.add_run(f"Controls: {refs}").font.size = Pt(9)
            if req.placeholders:
                ph_note = doc.add_paragraph(style="List Bullet 2")
                ph_note.add_run(f"Placeholders: {', '.join(req.placeholders)}").font.size = Pt(9)

    # 4. Roles and Responsibilities
    _hdr(doc, "4.  Roles and Responsibilities")
    for role in draft.roles_responsibilities:
        p = doc.add_paragraph(style="List Number")
        p.add_run(f"{role.item_no} ").bold = True
        p.add_run(role.text_en)

    # 5. Update and Review
    _hdr(doc, "5.  Update and Review")
    doc.add_paragraph(draft.review_update_en)

    # 6. Compliance with the Standard
    _hdr(doc, "6.  Compliance with the Standard")
    for i, clause in enumerate(draft.compliance_en, start=1):
        p = doc.add_paragraph(style="List Number")
        p.add_run(clause)

    # Closing Note
    _hdr(doc, "Note", level=2)
    doc.add_paragraph(draft.closing_note_en).runs[0].italic = True if doc.paragraphs[-1].runs else None

    doc.add_page_break()
    footer_p = doc.add_paragraph()
    footer_p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    footer_p.add_run(
        f"Generated by AutoAlign Policy Factory (Golden Standard Baseline) | "
        f"{datetime.now(timezone.utc).strftime('%Y-%m-%d')}"
    ).font.size = Pt(8)

    print(f"[Renderer] Writing standard -> {main_path}")
    doc.save(main_path)

    # ── Traceability Annex ────────────────────────────────────────────────────
    ann = Document()
    _hdr(ann, "Control Traceability Annex")
    ann.add_paragraph(
        f"Document: {draft.doc_id}  |  {draft.title_en}  |  Version: {draft.version}"
    ).runs[0].font.size = Pt(10)
    ann.add_paragraph(
        "All controls below were sourced via dense retrieval + cross-encoder reranking. "
        "Every 'shall' requirement must be backed by an NCA control. "
        "The 'Rerank Score' column shows the cross-encoder relevance score."
    ).runs[0].font.size = Pt(9)

    hdrs = ["Req ID", "Domain", "Requirement (excerpt)", "Framework", "Control ID", "Control Title", "NCA ID", "Rerank Score"]
    tbl2 = ann.add_table(rows=1, cols=len(hdrs))
    tbl2.style = "Table Grid"
    _tbl_hdr_row(tbl2, hdrs)

    for dom in draft.domains:
        # Domain separator row
        sep_row = tbl2.add_row()
        sep_cell = sep_row.cells[0]
        sep_cell.merge(sep_row.cells[-1])
        sep_cell.text = f"Domain {dom.domain_number}: {dom.title_en}"
        _set_cell_bg(sep_cell, _LIGHT)
        for para in sep_cell.paragraphs:
            for run in para.runs:
                run.bold = True
                run.font.size = Pt(9)

        for req in dom.requirements:
            for t in req.trace:
                row = tbl2.add_row()
                cells = row.cells
                cells[0].text = req.req_id
                cells[1].text = dom.title_en
                cells[2].text = req.statement_en[:200] + ("..." if len(req.statement_en) > 200 else "")
                cells[3].text = t.framework
                cells[4].text = t.control_id

                b = ctrl_map.get(t.control_id) or chunk_map.get(t.source_ref)
                cells[5].text = b.title if b else "-"
                cells[6].text = (b.nca_id or b.uae_ia_id or "-") if b else "-"
                score = b.rerank_score if b else None
                cells[7].text = f"{score:.4f}" if score is not None else "-"

                if score is not None and score > 5.0:
                    _set_cell_bg(cells[7], _GREEN)
                for cell in cells:
                    _small(cell)

    print(f"[Renderer] Writing standard traceability annex -> {annex_path}")
    ann.save(annex_path)

    total_reqs = sum(len(d.requirements) for d in draft.domains)
    print(f"[Renderer] Standard render done: {len(draft.domains)} domains, {total_reqs} requirements.")
    return main_path, annex_path


